import numpy as np
def incr_oct(base,octave):
    return base * 2 ** octave

def incr_db(base,db):
    return 10.0 ** ((db + 10.0 * np.log10(base)) / 10.0)

def incr_dba(base,db):
    return 10.0 ** ((db/2.0 + 10.0 * np.log10(base)) / 10.0)

